class XLog extends XCLass
{
/******************************************************/
/*****************     PARAMETRES     *****************/
/******************************************************/

	use XSystem;

	public function setClassProperties() {
		//$this->mainDatabaseTable('x_logs');
		//$this->mainDatabaseTableRef('');
		/*  				/!\		En cours de re-développement		/!\						*/
		$this->property('id',			'integer');
		$this->property('date');
		$this->property('ip_address',	'string');
		$this->property('type',			'integer');
		$this->property('criticity',	'integer');
		$this->property('code');
		$this->property('file', 		'string');
		$this->property('log_line',		'integer');
		$this->property('parameters');
	}

/**********************************************************/
/*****************     FIN PARAMETRES     *****************/
/**********************************************************/


/********************************************************/
/*****************     CONSTRUCTEUR     *****************/
/********************************************************/

	public function __construct(array $log_datas = array()) {			// Constructeur dirigé vers la méthode d'hydratation
		parent::__construct();
		$this->hydrate($log_datas);
	}

/************************************************************/
/*****************     FIN CONSTRUCTEUR     *****************/
/************************************************************/


/*******************************************************/
/*****************     HYDRATATION     *****************/
/*******************************************************/

	public function hydrate(array $log_datas) {
		foreach ($log_datas as $log_datas_key => $log_datas_value) {
			$this->set($log_datas_key, $log_datas_value);
		}
	}

/***********************************************************/
/*****************     FIN HYDRATATION     *****************/
/***********************************************************/


